#include "libft.h"

void	*ft_memmove(void *dest, void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (src >= dest)
	{
		while (i < n)
		{
			((unsigned char *)dest)[i] = ((unsigned char *)src)[i];
			i++;
		}
		return (dest);
	}
	while (--n + 1)
		((unsigned char *)dest)[n] = ((unsigned char *)src)[n];
	return (dest);
}
